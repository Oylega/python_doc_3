
def find_area():
    length = int(input("What is the area of the house?\n"))
    width = int(input("What is the width of the house?\n"))
    area = length * width
    return(area)


 

def find_circumference():
    import math
    pi = math.pi
    radius = int(input("What is the radius of the circle?\n"))
    circumference = 2 * pi * radius
    return(circumference)


